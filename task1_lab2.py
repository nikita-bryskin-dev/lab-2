money_capital = 20000  # Финансовая подушка безопасности
salary = 5000           # Ежемесячная зарплата
spend = 6000            # Траты за первый месяц
increase = 0.05         # Рост цен (5% ежемесячно)

# Переменная для подсчета количества месяцев
months = 0

# Цикл для расчета месяцев
while money_capital + salary >= spend:
    # Увеличиваем количество месяцев
    months += 1
    
    # Обновляем подушку безопасности
    money_capital += salary - spend
    
    # Увеличиваем расходы на 5% (начиная со второго месяца)
    spend *= (1 + increase)

# Вывод результата
print("Количество месяцев, которое можно протянуть без долгов:", months)